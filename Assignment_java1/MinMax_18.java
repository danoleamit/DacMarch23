class MinMax_18
{
  public static void main(String args[])
  {
     int num1=25;
     int num2=288;

     System.out.println("maximum number = "+Integer.max(num1,num2));
     System.out.println("minimum number = "+Integer.min(num1,num2));
     System.out.println("Addition number = "+Integer.sum(num1,num2));
  }

}